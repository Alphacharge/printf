#include "ft_printf.h"

int	ft_putstr(char *s, char c)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			ft_putchar_fd(s[i], 1);
			i++;
		}
	}
	else
	{
		write(1, "(null)", 6);
		return (6);
	}
	if (c == 'i' || c == 'd' || c == 'u')
		free (s);
	return (i);
}
